# QUESTION 5.1:
# You're having coffee/tea/beverage of your choice with a friend that is learning to program in Python.
# They're curious about why they would use pip.
# Explain what pip is and one benefit of using pip.

# ANSWER:
# pip is a package manager for Python.
# It is a tool that allows you to install and manage Python packages, that aren't part of the Python standard library.
# A Python package contains all the files you need for a module.
# Modules are Python code libraries, that other people have written, which you can include in your project.
# So, pip allows you to access ready-written code, which will save you a lot of time and energy when writing
# your own Python project, making your development process much more efficient.
